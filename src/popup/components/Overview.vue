<template>
  <div class="overview">
    <div class="title-tag-wrapper">
      <TransactionTag
        :tx-type="getTitle"
        data-cy="title"
      />
      <TransactionTag
        v-if="txFunction && getTxType"
        :tx-type="getTxType"
        class="title-tag"
        data-cy="tx-function"
      />
    </div>

    <div class="parties">
      <Avatar v-bind="sender" />
      <div class="mid">
        <TriangleRight />
        <div class="line" />
      </div>
      <Avatar
        v-if="recipient.address"
        v-bind="recipient"
      />
      <div
        v-else
        class="avatar"
      >
        <AensIcon
          v-if="recipient.aens"
          class="icon"
        />
        <ActionIcon
          v-else
          class="icon"
        />
      </div>
    </div>

    <div class="details">
      <div
        class="sender"
        data-cy="sender"
      >
        <a
          :href="sender.url"
          target="_blank"
          class="name"
        >
          <Truncate :str="sender.name || sender.label" />
        </a>
        <CopyText
          hide-icon
          :value="sender.address"
        >
          <span class="text-address text-left">{{ senderAddress }}</span>
        </CopyText>
      </div>
      <div
        class="recipient"
        data-cy="recipient"
      >
        <a
          v-if="recipient.url"
          :href="recipient.url"
          target="_blank"
          class="name"
        >
          <Truncate :str="recipient.name || recipient.label" />
        </a>
        <span
          v-else
          class="name"
          :class="{ aens: recipient.aens }"
        >
          {{ recipient.label }}
        </span>
        <CopyText
          v-if="recipient.address"
          hide-icon
          :value="recipient.address"
        >
          <span class="text-address">{{ recipientAddress }}</span>
        </CopyText>
      </div>
    </div>
  </div>
</template>

<script>
import Truncate from './Truncate.vue';
import CopyText from './CopyText.vue';
import TriangleRight from '../../icons/triangle-right.svg?vue-component';
import ActionIcon from '../../icons/action.svg?vue-component';
import AensIcon from '../../icons/aens.svg?vue-component';
import Avatar from './Avatar.vue';
import TransactionTag from './TransactionTag.vue';
import { DEX_TRANSACTION_TAGS } from '../utils';
import { FUNCTION_TYPE_DEX } from '../utils/constants';

export default {
  components: {
    Truncate,
    CopyText,
    TriangleRight,
    ActionIcon,
    AensIcon,
    Avatar,
    TransactionTag,
  },
  props: {
    title: { type: String, required: true },
    txFunction: { type: String, default: null },
    sender: { type: Object, required: true },
    recipient: { type: Object, required: true },
  },
  computed: {
    getTxType() {
      return this.$te(`transaction.dexType.${DEX_TRANSACTION_TAGS[this.txFunction] || this.txFunction}`)
        ? this.$t(`transaction.dexType.${DEX_TRANSACTION_TAGS[this.txFunction] || this.txFunction}`)
        : '';
    },
    getTitle() {
      return FUNCTION_TYPE_DEX.pool.includes(this.txFunction)
        ? this.$t('transaction.dexType.pool')
        : this.title;
    },
    senderAddress() {
      if (!this.sender.address.includes('ak_')) return this.sender.address;

      return this.sender.address.match(/.{1,3}/g).join(' ');
    },
    recipientAddress() {
      if (!this.recipient.address.includes('ak_')) return this.recipient.address;

      return this.recipient.address.match(/.{1,3}/g).join(' ');
    },
  },
};
</script>

